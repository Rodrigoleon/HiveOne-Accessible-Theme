				<h2>Man with disability building free wheelchair ramps for Montreal businesses</h2>
				<p>The wants and needs of people in wheelchairs are pretty much the same as anyone else, Omar Lachheb notes.</p>
				<p>“They want to go to restaurants, they want to have fun, they want to go with their friends and access stores and bars,” said the 37-year-old who has been in a wheelchair since the age of 16.</p>
				<p>The problem is that at the majority of businesses, bars and restaurants in Montreal, easy access is often hampered by one step a few centimetres high, making it either impossible to enter, or requiring the aid of a store employee or a friend and feeling like a burden, or an outsider.</p>
				<p>Which is why Lachheb is kick-starting a non-profit organization dubbed J’accède (I access) that will donate lightweight one-step ramps to businesses in Montreal. Four businesses are already on board, including the iconic St-Viateur Bagel in the Plateau and Gabytech computer services store on Parc Ave. Both have steps into their stores roughly 15 centimetres high.</p>
